// Math Methods and Properpties

console.log(Math.PI);
console.log(Math.trunc(Math.PI)); // remove decimals
console.log(Math.round(3.5)); // round up if +5 or stays the same
console.log(Math.floor(3.8)); // round always down
console.log(Math.pow(2, 10)); // power of a num
console.log(Math.min(2, 10, 5)); // returns the min 
console.log(Math.max(2, 10, 5)); // returns the largest
console.log(Math.random()) // always from 0 to 1

console.log(Math.floor(Math.random() * 10) + 1) // random num till 10. without the + 1 never will reach 10